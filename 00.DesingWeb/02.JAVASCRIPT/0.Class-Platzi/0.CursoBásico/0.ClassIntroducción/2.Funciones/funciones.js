// Las funciones son como un conjunto de sentencias que nosotros podemos utilizar para generar ciertas acciones con los valores que ya creamos antes en las var .

// Funciones declarativas
function miFuncion() {
    return 3;
}

miFuncion();

// Funciones de Expresion
var miFuncion = function(a,b){
    return a + b;
}

miFuncion();